// La boucle while
let index = 5;

while (index < 5) {
  console.log(index);
  index = index + 1;
}

// La boucle do-while
let counter = 5;

do {
  console.log(counter);
  counter = counter + 1;
} while(counter < 5);

// La boucle for
for (let index = 0; index < 5; index++) {
  console.log(index);
}

// 5
console.log(index);

