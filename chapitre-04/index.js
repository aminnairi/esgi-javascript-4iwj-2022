console.log("arithmétiques, comparaisons, logiques");

// Addition
console.log(1 + 2);

// Soustraction
console.log(1 - 2);

// Multiplication
console.log(1 * 2);

// Division
console.log(1 / 2);

// Division par zéro
console.log(1 / 0);

// Modulo
console.log(3 % 2);

// Modulo par zéro
console.log(3 % 0);

// Puissance de deux
console.log(10 ** 2);

// Affectation par l'addition
let result = 1;

// result = result + 2
result += 2;

// Affectation par la soustraction (result = result - 2)
result -= 2;

// Affectation par la multiplication (result = result * 2)
result *= 2;

// Affectation par la division (result = result / 2)
result /= 2;

// Affectation par modulo
result %= 2;

// Affectation par puissance (result = result ** 2)
result **= 2;

// Comparaison par la supériorité
console.log(1 > 2);

// Comparaison par l'infériorité
console.log(1 < 2);

// Comparaison par la supériorité ou l'égalité
console.log(1 >= 2);

// Comparaison par l'infériorité ou l'égalité
console.log(1 <= 2);

// Comparaison par l'égalité superficielle
console.log(1 == "1");

// Comparaison par l'égalité stricte
console.log(1 === "1");

// Comparaison par l'inégalité superficielle
console.log(1 != "1");

// Comparaison par l'inégalité stricte
console.log(1 !== "1");

// Comparaison logique ET
console.log(true && true);

// Comparaison logique OU
console.log(false || true);

// Négation
console.log(!true); // false
console.log(!false); // true
